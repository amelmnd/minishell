#include "../pipex.h"

int	ft_strncmp(const char *s1, const char *s2, int length)
{
	int	i;

	i = -1;
	while (++i < length && (s1[i] || s2[i]))
	{
		if (s1[i] != s2[i])
			return ((unsigned char)s1[i] - (unsigned char)s2[i]);
	}
	return (0);
}
